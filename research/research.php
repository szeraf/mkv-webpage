											<h3>Gyüjteményeink Cikk 1</h3>
											<p>Magyar Gyűjtők Társasága

												Balta, fejsze, szekerce és bárdgyűjtemény

												A gyűjtemény több mint 500 darabot tartalmaz a fenti kéziszerszámokból. A szerszámok döntő többsége a 19. század második feléből és a 20. század első feléből származik.

												A fő gyűjtési ág mellett  egyéb faipari kéziszerszámok (gyaluk, kézi vonók, vésők kalapácsok stb.) is megtalálhatók a gyűjteményben.

												A gyűjtő tagja a Magyar Gyűjtők Társaságának és a Magyar Famíves Céh Egyesületnek.

												Kapcsolat: recskae@pr.hu

												Szakirodalom:

												Recska Endre:  „Fafeldolgozó vasszerszámok védjegyei”  (Osztrák-Magyar Monarchia és Magyarország, 1876-1945) magánkiadás, 2007.

												Recska Endre: „Kések, borotvák, ollók, vágó- és szúrófegyverek védjegyei” (Osztrák-Magyar Monarchia és Magyarország, 1876-1945) magánkiadás 2011.

												„cme” Varrógép-gyűjtemény

												A 2000 óta létező „cme” gyűjtemény 60-70 db, a. gyűjtő által szakszerűen helyreállított varrógépből és egyéb kiegészítőkből ( olajozók, tartozékdobozok, korabeli dokumentumok, mint pl. számlák, használati utasítások) áll. A gyűjtemény erőssége a professzionális varrógép műszerészi háttér, mely másoknak is jó lehetőség a műszaki problémák elhárítására.

												A gyűjtő tagja a Magyar Gyűjtők Társaságának

												Kapcsolat: Méreg László, maskara45@freemail.hu
												
												A „cst” gyűjtemény

												A soproni székhelyű, bő negyven éve szaporodó, s ma már kb. 400 tárgyat számláló faipari szerszámgyűjtemény alapja egy 1968-ban megvásárolt háború előtti asztalos-műhely maradéka. Ez attól fogva folyamatosan bővült asztalos szerszámokkal és ügyeskezű, falusi fúró-faragó emberek egyedi szerszámaival. A cél egy minél teljesebb asztalos szerszámkészlet összeállítása volt, melyhez régi szakkönyvek, mintalapok, árjegyzékek, képek és a szakma múltját érintő könyvrészletek, leírások, adatok, gyűjtése is párosult. A gyűjtemény erőssége a rendszerezettség, a viszonylag bőséges régi szakirodalom – eredeti vagy másolt formában – és egyedi, ún. „sajátkészítésű” szerszámok.

												A gyűjtő tagja a Magyar Gyűjtők Társaságának a Magyar Famíves Céhnek, és a Faipari Tudományos Egyesületnek.

												Kapcsolat: Szemerey Tamás,  tszemerey@fmk.nyme.hu

												Szakirodalom:

												1. Szemerey T.: Az asztalosságban használt idegen szavak gyűjteménye,in: Faipari kézikönyv II. (szerk.: Molnárné Posch Paula) Sopron, 2002. p. 442-446. ISBN 963 00 4231 2; ISBN 963 00 4229 0

												2. Szemerey T: 350 db szakmatörténeti (az asztalosság történetét ismertető) címszó kidolgozása,in: Bútorművészeti lexikon (szerk.: Kiss Éva, Corvina Bp. 2005) ISBN 963 13 5411 3

												3. Szemerey T: 250 db szakszó értelmezése és 450 db, az asztalosságban használt idegen szó értelmezése, ill. magyarázata in: Magyar-német faipari értelmező kéziszótár 29-52. és 158-173. o. (Zala Megyei Vállalkozásfejlesztési Alapítvány Zalaegerszeg, 2005) ISBN 963 219 009 2

												4. Szemerey T: Szerszámábrázolások 1-2.in: Hírfa 2008/2 és 5. (XVIII. évf.) 26-28. és (XVIII. évf.) 30-32. ISSN 1418-432X

												A „czs” gyűjtemény

												A „czs” a nyírség egyik fejlődőképes gyűjteménye, mely már kiállítási háttérrel is rendelkezik. A tulajdonos technikai gyakorlata folytán a gyűjtemény varrógépeket és azok szerkezeti elemei közül hangsúlyosan állványlapokat tartalmaz. Az öntött állványlapok itt nemcsak technikatörténeti, hanem esztétikai jelentőséggel is bírnak, hiszen a korabeli öntvényeket gyakran díszítették gazdag ornamentikák. A dokumentált gyűjtemény mögött jól felszerelt műhely és jelentős lakatosipari, kézműves gyakorlat húzódik meg.

												Gyűjtő: Zomborszki Zsolt,  Magyar Gyűjtők Társasága tagja

												Kapcsolat: sewing1846@gmail.com

												A „cnj” gyűjtemény

												A „cnj” gyűjteményben elsősorban asztalosok által használt szerszámok találhatók. A gyaluk, vésők, fűrészek, fúrók, enyvező edények, mérőeszközök mellett a fafeldolgozó mesterségek (kádár, ács, bognár, faesztergályos) szerszámai teszik teljessé a kollekciót.

												A gyűjtő a szerszámokhoz kapcsolódva, mesterség bemutatókat is szervez, ahol az őskortól napjainkig használt, de elfelejtett famegmunkálási munkafolyamatokat rekonstruálják.

												A gyűjtő tagja a Magyar Gyűjtők Társaságának.

												Kapcsolat: Németh János, czudar.nemeth@freemail.hu

												A „ctg” gyűjtemény 250 db történelmi, elsősorban 19.századi varrógépet tartalmaz, mely 68 gyártót ölel fel a világ varrógépgyártásából, 1864-től 1950-ig. A gyűjtemény erényei közé sorolható, hogy több nemzetközi vonatkozásban is ritka darabot mondhat magának és szakszerű feldolgozottsága lehetőséget biztosít akár tudományos kutatásra is.

												A gyűjtő tagja a Magyar Gyűjtők Társaságának, a német Schlingenfaenger és az angol ISMACS társaságoknak.

												Kapcsolat: Tóth György,  bautex@enternet.hu

												Magyar nyelvű szakirodalom: Tóth György: Gyűszűtől a varrógépig, magánkiadás 2008, Tóth György: Varrógépek gyűjtése, magánkiadás 2009, Tóth György: A varrógép enciklopédiája, magánkiadás 2011</p>

